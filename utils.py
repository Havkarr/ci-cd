'''
File implements adding, subtracting, multiplying and division functions.
'''

def add(a: int, b: int) -> int:
    '''
    Function returns sum of two integers.
    :param a: (int) First number
    :param b: (int) Second number
    :return: (int) Sum of a and B
    '''
    return a + b


def subtract(a: int, b: int) -> int:
    '''
    Function subtracts given two numbers.
    :param a: (int) First number
    :param b: (int) Second number
    :return: (int) Difference of a and b
    '''
    return a - b


def multiply(a: int, b: int) -> int:
    '''
    Function multiply two given integers
    :param a: (int) First number
    :param b: (int) Second number
    :return: (int) Result of multiplication
    '''
    return a * b


def divide(a: int, b: int) -> float:
    '''
    Functions divide two given integers
    :param a: (int) First number
    :param b: (int) Second number
    :return: (int) Result of division
    '''
    return a / b
